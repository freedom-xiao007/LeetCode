    def wiggleMaxLength(self, nums: List[int]) -> int:
        size = len(nums)
        if size == 0:
            return 0

        pre, cur, ans = 0, 0, 1
        for i in range(1, size):
            cur = nums[i] - nums[i-1]
            if (cur < 0 <= pre) or (cur > 0 >= pre):
                ans += 1
                pre = cur
        # print(ans)
        return ans
