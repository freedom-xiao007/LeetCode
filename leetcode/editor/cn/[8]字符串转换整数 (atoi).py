    def __init__(self):
        self.INT_MAX = pow(2, 31) - 1
        self.INT_MIN = -pow(2, 31)

    def myAtoi(self, str: str) -> int:
        result = ""
        for c in str:
            type = self.judge(c)
            if type == 0:
                if result == '':
                    continue
                else:
                    break
            if type == 1:
                result += c
                continue
            if type == 2:
                if result == '':
                    result += c
                    continue
                else:
                    break
            if type == 3:
                if result == '':
                    return 0
                else:
                    break

        if result == '' or result == '+' or result == '-':
            return 0
        if int(result) > self.INT_MAX:
            return self.INT_MAX
        if int(result) < self.INT_MIN:
            return self.INT_MIN
        print(int(result))
        return int(result)

    def judge(self, c):
        if c == ' ':
            return 0
        if c == '0' or c == '1' or c == '2' or c == '3' or c == '4' or c == '5' or c == '6' or c == '7' or c == '8' or c == '9':
            return 1
        if c == '-' or c == '+':
            return 2
        return 3
