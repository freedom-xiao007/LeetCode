class Solution:
    """
    è§£é¢˜æ€è·¯ï¼š
    ä¸€ã€åŠ¨æ€è§„åˆ’ï¼šç±»ä¼¼å­—ç¬¦ä¸²å¢žåˆ çš„é‚£ä¸ªè¡¨æ ¼
    dp[i][j] ä»£è¡¨ T å‰ i å­—ç¬¦ä¸²å¯ä»¥ç”± S j å­—ç¬¦ä¸²ç»„æˆæœ€å¤šä¸ªæ•°.
    å½“ S[j] == T[i] , dp[i][j] = dp[i-1][j-1] + dp[i][j-1];
    å½“ S[j] != T[i] , dp[i][j] = dp[i][j-1]
    """
    def numDistinct(self, s: str, t: str) -> int:
        n1 = len(s)
        n2 = len(t)
        dp = [[0] * (n1 + 1) for _ in range(n2 + 1)]
        for j in range(n1 + 1):
            dp[0][j] = 1
        for i in range(1, n2 + 1):
            for j in range(1, n1 + 1):
                if t[i - 1] == s[j - 1]:
                    dp[i][j] = dp[i - 1][j - 1] + dp[i][j - 1]
                else:
                    dp[i][j] = dp[i][j - 1]
        # print(dp)
        return dp[-1][-1]


