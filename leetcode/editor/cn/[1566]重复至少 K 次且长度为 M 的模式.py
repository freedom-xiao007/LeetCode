    def containsPattern(self, arr: List[int], m: int, k: int) -> bool:
        size = len(arr)
        for i in range(0, size - m):
            sub = arr[i:i + m]
            count = 1
            start = i + m
            while start < size:
                if sub == arr[start: start + m]:
                    count += 1
                    start += m
                else:
                    break
            # print(sub, count)
            if count >= k:
                return True
        return False


