"""
126. 单词接龙 II
给定两个单词（beginWord 和 endWord）和一个字典 wordList，找出所有从 beginWord 到 endWord 的最短转换序列。转换需遵循如下规则：

每次转换只能改变一个字母。
转换后得到的单词必须是字典中的单词。
说明:

如果不存在这样的转换序列，返回一个空列表。
所有单词具有相同的长度。
所有单词只由小写字母组成。
字典中不存在重复的单词。
你可以假设 beginWord 和 endWord 是非空的，且二者不相同。
示例 1:

输入:
beginWord = "hit",
endWord = "cog",
wordList = ["hot","dot","dog","lot","log","cog"]

输出:
[
  ["hit","hot","dot","dog","cog"],
  ["hit","hot","lot","log","cog"]
]
示例 2:

输入:
beginWord = "hit"
endWord = "cog"
wordList = ["hot","dot","dog","lot","log"]

输出: []

解释: endWord "cog" 不在字典中，所以不存在符合要求的转换序列。


解题思路：
方法1：
对单词接龙I的单向BFS进行改写即可，需要注意的点有：
    wordlist，visited的查找优化（set哈希）
    栈中存单词和层级改为存完整路径
    使用26单个字母变换比通配符映射生成的匹配次数少

可以改进优化的地方，单向改双向
"""
from typing import List


class Solution:
    def findLadders(self, beginWord: str, endWord: str, wordList: List[str]) -> List[List[str]]:
        if endWord not in wordList:
            return []

        wordList = set(wordList)
        length = len(beginWord)
        visited = set()
        stack = [[beginWord]]
        ans = []
        found = False
        while stack:
            for words in stack:
                visited.add(words[-1])

            for i in range(0, len(stack)):
                words = stack.pop(0)
                for j in range(0, length):
                    for k in range(97, 123):
                        nextWord = words[-1][:j] + chr(k) + words[-1][j + 1:]
                        if nextWord not in wordList or nextWord in visited:
                            continue
                        if nextWord == endWord:
                            ans.append(words + [nextWord])
                            found = True
                        stack.append(words + [nextWord])
            if found:
                break
        return ans


