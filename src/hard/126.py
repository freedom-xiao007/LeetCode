        marked=set()
        queue=[[beginWord]]
        while queue:
            temp=[]
            found=False
            for words in queue:
                marked.add(words[-1])
            for words in queue:
                for w in edges(words[-1]):
                    v=words+[w]
                    if w == endWord:
                        res.append(v)
                        found=True
                    temp.append(v)
            if found:          #找到就不再遍历了，即使再有endWord，路径也会更长
                break
            queue=temp
        return res


